    /// </summary>
    Planning = 2,

    /// <summary>
    /// Phase 3: Execution of the approved plan.
    /// </summary>
    Execution = 3,

    /// <summary>
    /// Phase 4: Delivery of the final result.
    /// </summary>
    Delivery = 4
}
